package class07;

public class ForLoop {
    public static void main (String[]args){
         // i want to say good morning 5 times
        for (int i=1; i<=5; i++){
            System.out.println("good morning");
        }
        System.out.println("-------------------------------");

        // print numbers from 1 to 90

        for (int num =1 ; num<=90; num++){
            System.out.print(num+" ");
        }
        System.out.println("-------------------------");
        // print numbers from 20 to 1
        for (int a=20; a>=1; a--){
            System.out.print(a+" ");

        }
    }
}
