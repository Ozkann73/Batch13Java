package class07;

public class Loops {
    public static void main(String[] args) {
        int time = 10;

        while (time < 17) {
            System.out.println("hello");
            time++;
        }
        // print numbers from 1-10
        int num = 1;
        while (num <= 10) {
            System.out.println(num);
            num++;

        }

    }
}
