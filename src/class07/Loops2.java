package class07;

public class Loops2 {
    public static void main(String[] args) {
        // print numbers from 1-100
        int num = 1;
        while (num <= 100) {
            System.out.print(num + " ");
            num++;
        }
        System.out.println("----------------------------");
        // print numbers from 10-1
        int num1 = 10;
        while (num1 >= 1) {
            System.out.print(num1 + " ");
            num1--;
        }
        System.out.println("-----------------------------------");
        // print from 100 to 50
        int i = 100;
        while (i >= 50) {
            System.out.print(i + " ");
            i--;
        }

    }
}
